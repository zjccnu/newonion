package bean;

public class UserModel {
   private boolean monitor;
   private String roleCount;
   private String roleKeyS;
   private String roleValueS;
   private String status;
   private boolean usable;
public boolean getMonitor() {
	return monitor;
}
public void setMonitor(boolean monitor) {
	this.monitor = monitor;
}
public String getRoleCount() {
	return roleCount;
}
public void setRoleCount(String roleCount) {
	this.roleCount = roleCount;
}
public String getRoleKeyS() {
	return roleKeyS;
}
public void setRoleKeyS(String roleKeyS) {
	this.roleKeyS = roleKeyS;
}
public String getRoleValueS() {
	return roleValueS;
}
public void setRoleValueS(String roleValueS) {
	this.roleValueS = roleValueS;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public boolean getUsable() {
	return usable;
}
public void setUsable(boolean usable) {
	this.usable = usable;
}
   
}
